math.randomseed(os.time())
map = {}

function love.load()
	love.graphics.setBackgroundColor(0, 0, 0)

	resetMap()

	timer = 0

	playerX = 10
	playerY = 10
	playerLength = 2
	playerDirection = "right"

	moves = {
		"right",
		"none"
	}
end

function love.update(dt)
	timer = timer + dt

	if timer >= 0.1 then 
		timer = 0

		for y = 1, 30 do 
			for x = 1, 40 do
				if map[y][x] > 0 then 
					map[y][x] = map[y][x] - 1 
				end
			end
		end

		if moves[1] == "right" then 
			playerX = playerX + 1
		elseif moves[1] == "left" then 
			playerX = playerX - 1
		elseif moves[1] == "up" then 
			playerY = playerY - 1
		elseif moves[1] == "down" then
			playerY = playerY + 1
		end
		if moves[2] ~= "none" then 
			moves[1] = moves[2]
			moves[2] = "none"
		end

		if playerX == 0 then 
			playerX = 40
		elseif playerX > 40 then 
			playerX = 1
		end
		if playerY == 0 then 
			playerY = 30
		elseif playerY > 30 then 
			playerY = 1
		end

		if map[playerY][playerX] == -1 then 
			playerLength = playerLength + 1
			--map[playerX][playerY] = 0
			map[math.random(1, 30)][math.random(1, 40)] = -1
		end



		if map[playerY][playerX] > 0 then  -- Die
			playerLength = 2
			playerX = 10
			playerY = 10
			resetMap()
		end

		map[playerY][playerX] = playerLength

	end

	

end

function love.draw()

	love.graphics.setColor(1,1,1)
	for y = 1, 30 do 
		for x = 1, 40 do
			if map[y][x] > 0 then -- Alt over 0 betyder at det er slangen
				love.graphics.rectangle("fill", (x-1)*20, (y-1)*20, 20, 20)
			elseif  map[y][x] == -1 then -- -1 repræsenterer mad
				love.graphics.setColor(0,1,0)
				love.graphics.rectangle("fill", (x-1)*20, (y-1)*20, 20, 20)
				love.graphics.setColor(1,1,1)
			end
		end
	end


	love.graphics.setColor(0,0,0) --Her tegner vi et gitter af sorte streger. Det får spillet til at se ud som om det er lavet af firkanter
	for i = 0, 40 do
		love.graphics.line(i*20, 0, i*20, 600)
	end
	for i = 0, 30 do
		love.graphics.line(0, i*20, 800, i*20)
	end
end


function love.keypressed(key)
	if (key == "up" 	or key == "w" ) and moves[1] ~= "down" then 
		moves[2] = "up"
	elseif (key == "down"  or key == "s")  and moves[1] ~= "up" then 
		moves[2] = "down"
	elseif (key == "left"  or key == "a")  and moves[1] ~= "right" then 
		moves[2] = "left"
	elseif (key == "right" or key == "d" ) and moves[1] ~= "left"then 
		moves[2] = "right"
	end

	if key == "escape" then 
		love.event.quit()
	end
end

